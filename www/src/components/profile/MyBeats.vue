<template>
  <div class="container myBeats">
    <div class="row">
      <div class="col-2 mr-2">
        <div class="">
          <label class="switch">
            <input type="checkbox" v-model="shared" >
            <span class="slider round"></span>
          </label>
          <p class="text-center">
            <!-- {{project.privacySetting}} -->
            <span v-if="!shared" >Private</span>
            <span v-if="shared" >Shared</span>
          </p>
        </div>
      </div>
      <div class="col-6">
        <h5 @click="showStats = showStats ? false : true" class="btn-link text-white text-center">{{project.title}} =</h5>
      </div>
      <div class="col-1">
        <button @click='closeTrackStats' class="btn btn-sm btn-dark">></button>
      </div>
      <div class="col-1">
        <button @click='deleteProject(project)' :disabled="disable" class="btn btn-sm btn-danger">Delete</button>
      </div>
    </div>
    <div class="trackStats" v-if="showStats">
      <div class="row">
        <div class="col text-center">
          <p>Plays: {{project.playCount}}</p>
        </div>
        <div class="col text-center">
          <p>Forked: {{project.forkCount}}</p>
        </div>
        <div class="col text-center">
          <p>Shared: {{project.shareCount}}</p>
        </div>
      </div>
      <div class="row">
        <div class="col">
          <p class="text-center">{{project.description}}</p>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
  export default {
    name: 'Mybeats',
    data() {
      return {
        showStats: false,
        shared: this.project.privacySetting,
        // btnDisable: false
      
      }
    },
    computed: {
      disable() {
        return this.$store.state.userProjects.length === 1
      },
      
    },
    watch: {
      shared: function(shared) {
        // console.log('adsf',shared,this.project)
        var payload = [this.project]
        if (shared == true) {
          var newStatus = true
          // var sharedProject = payload.privacySetting
          payload.push(newStatus)
          // console.log(payload,"payload shared")
          this.$store.dispatch('getProjectPrivacy', payload)
        } else {
          var newStatus = false
          // var NotSharedProject = payload.privacySetting
          payload.push(newStatus)
          // console.log(payload,"payload private")
          this.$store.dispatch('getProjectPrivacy', payload)
        }
      }
    },
    props: [
      'project'
    ],
    methods: {
      closeTrackStats() {
        this.showStats = false
      },
      showTrackStats() {
        this.showStats = true
      },
   
      deleteProject(project) {
        this.$store.dispatch('deleteProject', project)
      }
    }
  }
</script>

